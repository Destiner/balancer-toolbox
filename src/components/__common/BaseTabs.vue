<template>
  <div class="radio">
    <BaseLabel
      v-if="label"
      :value="label"
      :target="id"
      :disabled="disabled"
    />
    <RadioGroup
      :model-value="selectedOption"
      @update:model-value="handleUpdate"
    >
      <RadioGroupLabel class="caption">Select an option</RadioGroupLabel>
      <div class="list">
        <RadioGroupOption
          v-for="option in options"
          v-slot="{ active }"
          :key="option.value"
          as="template"
          :value="option.value"
        >
          <div
            :class="{ active, checked: option.value === modelValue }"
            class="option"
          >
            <RadioGroupLabel
              as="p"
              :class="{ checked: option.value === modelValue }"
              class="label"
            >
              {{ option.label }}
            </RadioGroupLabel>
          </div>
        </RadioGroupOption>
      </div>
    </RadioGroup>
  </div>
</template>

<script setup lang="ts">
import { RadioGroup, RadioGroupLabel, RadioGroupOption } from '@headlessui/vue';
import { computed } from 'vue';

import BaseLabel from './BaseLabel.vue';

const props = defineProps<{
  modelValue: string;
  options: Option[];
  disabled?: boolean;
  label?: string;
}>();

const emit = defineEmits<{
  (e: 'update:modelValue', value: string): void;
}>();

const id = computed(() => `select-${Math.random().toString().substring(2)}`);

const selectedOption = computed<Option>(() => {
  return props.options.find(
    (option) => option.value === props.modelValue,
  ) as Option;
});

function handleUpdate(value: string): void {
  emit('update:modelValue', value);
}
</script>

<script lang="ts">
interface Option {
  value: string;
  label: string;
}

// eslint-disable-next-line import/prefer-default-export
export { Option };
</script>

